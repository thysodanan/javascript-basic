function displayplace(array) {
  const container = document.querySelector(".container");
  const place = array
    .map(function (arr) {
      return `
        <article class="card">
        <img
          class="img"
          src="${arr.img}"
          alt="Angkor wat"
        />
        <h3 class="name">${arr.title}</h3>
        <p class="idea">
          ${arr.description}
        </p>
      </article>
        `;
    })
    .join("");
  container.innerHTML = place;
}
displayplace(turistPlaces);

function displaybtn() {
  const btn = document.querySelector(".btn-header");
  const button = turistPlaces.map((item) => item.country);
  const Country = new Set(button); //select only one country in all place in one country
  const newbtn = ["All", ...Country]; //convert to array
  const inputallbtn = newbtn
    .map((p) => {
      return `
          <button class="btn" data-id="${p}">${p}</button>
      `;
    })
    .join("");
  btn.innerHTML = inputallbtn;
  const allbtn = document.querySelectorAll(".btn");
  allbtn.forEach(function (btnitem) {
    btnitem.addEventListener("click", function () {
      const id = this.dataset.id;
      const filterarr = turistPlaces.filter(function (i) {
        return i.country === id;
      });
      if (id === "All") {
        displayplace(turistPlaces);
      } else {
        displayplace(filterarr);
      }
      const input = document.querySelector(".input");
      input.value = "";
    });
  });
}
displaybtn();

function searchitem() {
  const input = document.querySelector(".input");
  input.addEventListener("keyup", function () {
    const valueinput = input.value.toLowerCase(); //tolowercase() is mean that all value capital character and small is input can know and output it out
    const filterarr = turistPlaces.filter(function (item) {
      return item.title.toLowerCase().includes(valueinput);
    });
    displayplace(filterarr);
  });
}
searchitem();
